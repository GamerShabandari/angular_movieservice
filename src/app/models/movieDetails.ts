export class movieDetails {

    title:string
    poster:string
    genre:string
    year:string
    runtime:string
    rated:string
    plot:string
    director:string
    ratings:string
    
    constructor(title:string, poster:string, genre:string, year:string, runtime:string, rated:string, plot:string, director:string, ratings:string){

        this.title = title
        this.poster = poster
        this.genre = genre
        this.year = year
        this.runtime = runtime
        this.rated = rated
        this.plot = plot
        this.director = director
        this.ratings = ratings

    }

}